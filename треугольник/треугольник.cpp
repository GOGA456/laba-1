#include <iostream>
#include <cmath>

using namespace std;

int main() {
    double a, b, c;

    // Ввод сторон треугольника
    cout << "Введите длину стороны a: ";
    if (!(cin >> a) || a <= 0) {
        cout << "Ошибка: сторона a должна быть положительным числом." << endl;
        return -1;
    }

    cout << "Введите длину стороны b: ";
    if (!(cin >> b) || b <= 0) {
        cout << "Ошибка: сторона b должна быть положительным числом." << endl;
        return -1;
    }

    cout << "Введите длину стороны c: ";
    if (!(cin >> c) || c <= 0) {
        cout << "Ошибка: сторона c должна быть положительным числом." << endl;
        return -1;
    }

    // Проверка на существование треугольника
    if (a + b > c && a + c > b && b + c > a) {
        // Вычисление периметра
        double perimeter = a + b + c;
        cout << "Периметр треугольника: " << perimeter << endl;

        // Вычисление площади по формуле Герона
        double s = perimeter / 2;
        double area = sqrt(s * (s - a) * (s - b) * (s - c));
        cout << "Площадь треугольника: " << area << endl;

        // Проверка на равнобедренность
        if (a == b || a == c || b == c) {
            cout << "Треугольник является равнобедренным." << endl;
        } else {
            cout << "Треугольник не является равнобедренным." << endl;
        }
    } else {
        cout << "Треугольник с такими сторонами не существует." << endl;
        return -1;
    }

    return 0;
}